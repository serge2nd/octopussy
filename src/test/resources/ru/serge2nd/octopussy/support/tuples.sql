SELECT
  CAST(0 AS BIT) AS C1,
  CAST(-128 AS TINYINT) AS C2,
  CAST(-32768 AS SMALLINT) AS C3,
  CAST(-2147483648 AS INT) AS C4,
  CAST(-9223372036854775808 AS BIGINT) AS C5,
  CAST(-9223372036854775807.65 AS DECIMAL(21, 2)) AS C6,
  CAST(-999.9999 AS REAL) AS C7,
  CAST(-99999999.99999999 AS DOUBLE) AS C8,
  CAST('21:30:16' AS TIME) AS C9,
  CAST('2020-03-22' AS DATE) AS C10,
  CAST('2019-01-09T15:08:00' AS TIMESTAMP) AS C11,
  CAST(X'FEDCBA' AS VARBINARY) AS C12,
  CAST('fedcba' AS VARCHAR) AS C13,
  CAST(X'FEDCBA' AS BLOB) AS C14,
  CAST('fedcba' AS CLOB) AS C15
UNION SELECT
  CAST(1 AS BIT) AS C1,
  CAST(127 AS TINYINT) AS C2,
  CAST(32767 AS SMALLINT) AS C3,
  CAST(2147483647 AS INT) AS C4,
  CAST(9223372036854775807 AS BIGINT) AS C5,
  CAST(9223372036854775807.65 AS DECIMAL(21, 2)) AS C6,
  CAST(999.9999 AS REAL) AS C7,
  CAST(99999999.99999999 AS DOUBLE) AS C8,
  CAST('15:30:16' AS TIME) AS C9,
  CAST('2018-09-24' AS DATE) AS C10,
  CAST('2019-01-01T01:45:38' AS TIMESTAMP) AS C11,
  CAST(X'ABCDEF' AS VARBINARY) AS C12,
  CAST('abcdef' AS VARCHAR) AS C13,
  CAST(X'ABCDEF' AS BLOB) AS C14,
  CAST('abcdef' AS CLOB) AS C15
ORDER BY C1 ASC
